int	isalnum(int c)
{
	if (c != '\0')
	{
		if (((c >= 'a' && c<= 'z')
				|| (c >= 'A' && c <= 'Z'))
					|| (c >= '0' && c <= '9'))
		{
			return (1);
		}
	}
	return (0);
}

#include <stdio.h>
int	main()
{
	char c = 'g';
	if (isalnum(c))
	{
		printf("is alnum");
	}
	else
		printf("no alnum");
}