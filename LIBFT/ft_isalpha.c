#include "libft.h"

int	ft_isalpha(int c)
{
	if (c != '\0')
	{
		if (((c >= 'a' && c <= 'z')
				|| (c >= 'A' && c <= 'Z')))
		{
			return (1);
		}
	}
	return (0);
}
